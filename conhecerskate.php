                    <h2>o que é?</h2>
                                        
<p>O skate é um desporto inventado nos anos 50 na califórnia, EUA, que consiste em deslizar sobre o solo e obstáculos equilibrando-se numa tábua, o deck. Esse deck está agarrado a quatro rodas com rolamentos pelos trucks (eixos). A sua evolução tem sido brutal em termos de indústria e praticantes desde os anos 90 até a actualidade. Estimam-se haver mais de 50 milhões de skaters em todo o mundo, 13 milhões só nos estados unidos!</p>  
                   <h2>manobras básicas.</h2>
					<ul>
                    <li><b>Manual:</b> Consiste em andar em duas das quatro rodas do skate. se for feito com o corpo inclinado para a frente , a fazer força no nose (concavo frontal do skate) chama-se nosemanual, caso contrário, se for feito com o corpo inclinado para trás, fazendo força no tail (concavo da parte de trás do skate, ligeiramente mais baixo) chama-se manual.</li>
                        <li><b>Ollie:</b> Manobra base e essencial para street e vert. Consiste num salto em que os pés ficam agarrados à tábua.</li>
						
						<li><b>Pop Shove it:</b> É uma manobra onde o skate faz um angulo de 180 graus e o corpo fica na mesma posição. O truque é fazer a força certa para a esquerda no tail para o skate rodar e voltar a agarrar aos pés.</li>
                        <li><b>Kickflip: </b>Muitas vezes considerada a manobra máis frustrante e de dificil aprendizagem, consiste em raspar o pé da frente na diagonal no skate ao mesmo tempo que se faz força para baixo com o pé de baixo como se tratasse de um ollie. O skate faz assim uma volta a si mesmo e volta a aterrar nos pés.</li>
                       
                      </ul>
                       <h2>estilos.</h2>
<ul>
                    <li><b>Street:</b> Estilo de skate mais popular hoje em dia. Consiste em andar de skate nas ruas e em obstáculos que se assemelhem a ruas (skateparks) Corrimões, ledges ou curbs, rampas, escadas, manual pads, etc.</li>
                        <li><b>Vert:</b> Estilo de skate praticado em rampas. A maior parte das manobras são aéreas ou nos compings (ferros no topo das rampas) As rampas, chamadas halfpipes, são em forma de U e têem 3,40m ou mais de altura. Tony Hawk foi um
                  dos atletas mais conhecidos e que popularizou mais este estilo. </li>
						
						<li><b>Freestyle:</b> Este trata-se de um estilo menos praticado mas de extrema complexidade. Consiste em dar manobras geralmente em sequencia exclusivamente no chão, com variadissimas combinações.</li>
                        <li><b>tranny/pool: </b>Modo de skate que se assemelha ao vert mas praticado em minirampas ou em Bowls (bacia) que se assemelham a piscinas sem água.</li>
                        <li><b>Downhill:</b>  Esta modalidade consiste em fazer descidas com skates de shapes (formas) diferentes, geralmente mais compridos e largos, e com rodas e rolamentos diferentes. Estas adaptações servem para ajudar o skater nas descidas a alta velocidade.</li>
                       
                      </ul>
                      
